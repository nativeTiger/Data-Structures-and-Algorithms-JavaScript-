//References for JavaScript array methods.
/**
 * Arrays in JavaScript are modified objects, meaning that every array we
 * create has a few methods available to be used.
 * Some of them are listed below:
 * 
 * 
 *     *******************************************************************************************
 *     | Method             * Description                                                        |
 *     *******************************************************************************************
 *     | concat             * Joins multiple arrays and returns a copy of the joined arrays.     |
 *     *_________________________________________________________________________________________*
 *     | every              * Iterates every elements of the array, verifying the desired,       |
 *     |                    * condition (function) until false is returned.                      |
 *     *_________________________________________________________________________________________*
 *     | filter             * Creates an array with each element that evaluates to true in the   *
 *     |                    * function provided.                                                 |
 *     |_________________________________________________________________________________________*
 *     * forEach            * Executes a specific function on each element of the array.         |
 *     *_________________________________________________________________________________________*
 *     | join               * Joins all the array elements into a string.                        |
 *     *_________________________________________________________________________________________*
 *     | indexOf            * Searches the array for specific elements and returns its position. |
 *     *_________________________________________________________________________________________*
 *     | lastIndexOf        * Returns the position of the last item in the array that matches the|
 *     |                    * search criterion.                                                  |
 *     *_________________________________________________________________________________________*
 *     | map                * Creates a new array from a function that contains the criterion/   |
 *     |                    * condition and returns the elements of the array that match the     |
 *     |                    * criterion.                                                         |
 *     *_________________________________________________________________________________________*
 *     | reverse            * Reverses the array so that the last item becomes the first and vice|
 *     |                    * versa.                                                             |
 *     *_________________________________________________________________________________________*
 *     | slice              * Returns a new array form the specified index.                      |
 *     *_________________________________________________________________________________________*
 *     | some               * Iterates every element of the array, verifying the desired         |
 *     |                    * condition(function) until true is returned.                        |
 *     *_________________________________________________________________________________________*
 *     | sort               * Sorts the array alphabetically or by the supplied function.        |
 *     *_________________________________________________________________________________________*
 *     | toString           * Returns the array as a string.                                     |
 *     *_________________________________________________________________________________________*
 *     | valueOf            * Similar to the toString method, returns the array as a string.     |
 *     *_________________________________________________________________________________________*      
 */